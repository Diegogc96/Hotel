    private void armarCabecera() {
        modelo.addColumn("Dni");
        modelo.addColumn("Apellido");
        modelo.addColumn("Nombre");

        modelo.addColumn("Domicilio");
        modelo.addColumn("Celular");
        modelo.addColumn("Correo");
        modelo.addColumn("Estado");
        jTtabla.setModel(modelo);
    }

    public void armarTablaHuespedTotal() {
        borrarFilas();
        HuespedData huespedData = new HuespedData();

        for (Huesped huesped : huespedData.listarHuesped()) {

            modelo.addRow(new Object[]{huesped.getDni(), huesped.getApellido(), huesped.getNombre(), huesped.getDomicilio(), huesped.getCelular(), huesped.getCorreo(), huesped.isEstado()});

        }

    }

    public void armarTablaHuespedPorCaracter(String palabra) {
        borrarFilas();
        HuespedData huespedData = new HuespedData();

        for (Huesped huesped : huespedData.listarHuespedPorCaracter(palabra)) {
            modelo.addRow(new Object[]{huesped.getDni(), huesped.getApellido(), huesped.getNombre(), huesped.getDomicilio(), huesped.getCelular(), huesped.getCorreo(), huesped.isEstado()});
        }
    }

    private void borrarFilas() {
        int filas = jTtabla.getRowCount() - 1;
        for (int f = filas; f >= 0; f--) {
            modelo.removeRow(f);
        }
    }
